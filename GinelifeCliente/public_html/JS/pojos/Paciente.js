    this.correo = correo;
    this.entidad = entidad;
    this.municipio = municipio;
    this.colonia = colonia;
    this.calle = calle;
    this.numero = numero;
    this.codigoPostal = codigoPostal;
    this.idDoctora = idDoctora;
